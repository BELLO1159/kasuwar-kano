@app.route("/", methods=["GET", "POST"])
def index():
    if request.method == "POST":
        name = request.form.get("name")
        phone = request.form.get("phone")
        property_type = request.form.get("property_type")

        if name and phone:
            conn = sqlite3.connect("kasuwar-kano.db")
            cursor = conn.cursor()
            cursor.execute(
                "INSERT INTO leads (name, phone, property_type) VALUES (?, ?, ?)",
                (name, phone, property_type)
            )
            conn.commit()
            conn.close()
            flash("Thank you! Your inquiry has been received. Our agency representative will contact you shortly.")
            return redirect(url_for("index"))

    return render_template_string(HTML_TEMPLATE)

